package jungol.연산자;

public class Operator_9021_ex3 {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        System.out.printf("최초값 a = %1$d, b = %2$d \n\n", a, b);
        System.out.printf("a++ = %1$d, ++b = %2$d \n", a++, ++b);
        System.out.printf("실행후 a = %1$d, b = %2$d \n\n", a, b);
        System.out.printf("a-- = %1$d, --b = %2$d \n", a--, --b);
        System.out.printf("실행후 a = %1$d, b = %2$d \n", a, b);

    }
}
