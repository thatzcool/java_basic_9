package jungol.반복제어문2;

import java.util.Scanner;
public class loop2_9046_ex1 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int input = scanner.nextInt();
        int i = 1;

        while (i <= input) {
            System.out.println("C언어 프로그래밍");
            ++i;
        }
    }
}
