package jungol.반복제어문2;

import java.util.Scanner;

public class loop2_541_sefl1 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String input = scanner.nextLine();
        int i = 1;
        while(i<=20){
            System.out.print(input);
            ++i;
        }
    }
}
