package javabasic_02.day11.inheritance.sec01;

public class AA extends Object {
    private int aa = 10;
    private int bb = 20;

    public void printAA() {
        System.out.print(aa);
    }

    public int getAA() {
        return aa;
    }

    public int getBb() {
        return bb;
    }

}
