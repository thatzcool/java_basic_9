package javabasic_01.jungol.입력;

public class 입력_연습문제5 {

    public static void main(String[] args) {

          float x= 1.2340f;
          float y= 10.3459f;
        System.out.printf("전체 7자리로 맞추고 소수4자리까지 출력\n");
        System.out.printf("x =  %.4f\n",x);
        System.out.printf("x = %.4f\n\n",y);

        System.out.printf("소수 2자리까지 출력(반올림)\n");
        System.out.printf("x = %.2f\n",x);
        System.out.printf("y = %.2f\n",y);


    }
}
